    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        String outputEnkripsi = null, inputText = stringText.getText();
        Enkripsi classProgram = new Enkripsi();
        
        if (inputText.equals("")) {
            showMessageDialog(null, "Input string is empty!");
        } else {
            switch (typeHash.getSelectedIndex()){
                case 0: // MD5
                    outputEnkripsi = classProgram.encryptMD5(inputText);
                    break;
                case 1: // SHA1
                    outputEnkripsi = classProgram.encryptSHA1(inputText);
                    break;
                case 2: // SHA512
                    outputEnkripsi = classProgram.encryptSHA512(inputText);
                    break;
                default:
                    break;
            }
            
            if (outUppercase.isSelected()) {
                jTextArea1.setText(outputEnkripsi.toUpperCase());
            } else {
                jTextArea1.setText(outputEnkripsi);
            }
        }
    }                                        

