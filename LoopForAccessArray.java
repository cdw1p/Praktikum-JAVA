package loopforaccessarray;

/**
 *
 * @author 1337
 */
public class LoopForAccessArray {
    public static void main(String[] args) {
        int[] arrayOfInt = {32, 3, 89, 900, 1082, 20, 30, 127};
        
        for (int i=0; i<arrayOfInt.length; i++) {
            System.out.print(arrayOfInt[i]+" ");
        }
        System.out.println();
    }
}
